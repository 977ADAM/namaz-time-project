const RTL_LANGUAGES = new Set<AppLanguage>(["ar"]);

export function t(language: AppLanguage, key: string): string {
  return TRANSLATIONS[language][key] ?? TRANSLATIONS.ru[key] ?? key;
}

export function getLocaleCode(language: AppLanguage): string {
  return LANGUAGE_LOCALES[language];
}

export function getDirection(language: AppLanguage): "ltr" | "rtl" {
  return RTL_LANGUAGES.has(language) ? "rtl" : "ltr";
}

export function applyDocumentTranslations(language: AppLanguage): void {
  document.documentElement.lang = language;
  document.documentElement.dir = getDirection(language);

  document.querySelectorAll<HTMLElement>("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    if (key) {
      node.textContent = t(language, key);
    }
  });

  document.querySelectorAll<HTMLInputElement>("[data-i18n-placeholder]").forEach((node) => {
    const key = node.dataset.i18nPlaceholder;
    if (key) {
      node.placeholder = t(language, key);
    }
  });
}

export function getRouteTitle(language: AppLanguage, pathname: string): string {
  return t(language, `route.${pathname}`) || t(language, "app.title");
}
